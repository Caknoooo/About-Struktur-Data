void bst_init(BST *bst) {
    bst->_root = NULL;
    bst->_size = 0u;
}

bool bst_isEmpty(BST *bst) {
    return bst->_root == NULL;
}

bool bst_find(BST *bst, int value) {
    BSTNode *temp = __bst__search(bst->_root, value);
    if (temp == NULL)
        return false;
    
    if (temp->key == value)
        return true;
    else
        return false;
}

void bst_insert(BST *bst, int value) {
    if (!bst_find(bst, value)) {
        bst->_root = __bst__insert(bst->_root, value);
        bst->_size++;
    }
}

void bst_remove(BST *bst, int value) {
    if (bst_find(bst, value)) {
        bst->_root = __bst__remove(bst->_root, value);
        bst->_size--;
    }
}

void bst_inorder(BST *bst) {
    __bst__inorder(bst->_root);
}

void bst_postorder(BST *bst) {
    __bst__postorder(bst->_root);
}

void bst_preorder(BST *bst) {
    __bst__preorder(bst->_root);
}

void gasPreorder(BSTNode *root, set<int> &node)
{
    if(root)
    {
        gasPreorder(root->left, node);
        gasPreorder(root->right, node);
        if(root->left == NULL && root->right == NULL && node.find(root->key) != node.end())
            node.erase(root->key);
    }
}

void bst_removee(BSTNode *root, set<int> &node)
{
    if(root == NULL)
        return;
        
    BSTNode *temp = root;
    
    if(!temp)
        return;
        
    queue<BSTNode*> q;
    q.push(temp);
    
    while(!q.empty())
    {
        int sz = q.size();
        for(int i = 1; i <= sz; i++)
        {
            BSTNode *temp2 = q.front();
            q.pop();
            if(i == 1 || i == sz)
                node.erase(temp2->key);
            if(temp2->left != NULL)
                q.push(temp2->left);
            if(temp2->right != NULL)
                q.push(temp2->right);
        }
    }
    gasPreorder(root, node);
}
 
